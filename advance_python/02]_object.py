# Object

# - An object is an instance of a class 
# - After a class is established , you make object based on it.

#---------------------------------------------------------------------------------------------------------------------------------------

# syntax :- 

# objectName = ClassName()          for e.g :- s1 = student()

#---------------------------------------------------------------------------------------------------------------------------------------

# Access class attribute using object

# - We use '.(dot)' operator to access attribute of class.

# syntax :- 

# objectName.attributeName      (this will call the particular attribute within the class associated with the object)

