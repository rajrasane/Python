# Datatypes

'''  
Python has several built-in data types:

Numeric Types: int, float, complex
Sequence Types: str, list, tuple
Mapping Type: dict
Set Types: set, frozenset
Boolean Type: bool
Binary Types: bytes, bytearray, memoryview 
'''

# Numeric Types
a = 10          # int
b = 20.5        # float
c = 1 + 2j      # complex

# Sequence Types
d = "Hello"     # str
e = [1, 2, 3]   # list
f = (1, 2, 3)   # tuple

# Mapping Type
g = {"key": "value"}  # dict

# Set Types
h = {1, 2, 3}   # set
i = frozenset({1, 2, 3})  # frozenset

# Boolean Type
j = True        # bool
