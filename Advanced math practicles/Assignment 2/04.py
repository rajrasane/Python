# Write a Python program to sort a list of dictionaries using Lambda.

# Sorting the List of dictionaries :

# [{'make': 'Nokia', 'model': 216, 'color': 'Black'}, {'make': 'Samsung', 'model': 7, 'color': 'Blue'}, {'make': 'Mi Max', 'model': '2', 'color':Â 'Gold'}]


ls = [{'make': 'Nokia', 'model': 216, 'color': 'Black'}, {'make': 'Mi Max', 'model': '2', 'color': 'Gold'}, {'make': 'Samsung', 'model': 7, 'color': 'Blue'}]

print(sorted(ls,key=lambda x:x['color']))

